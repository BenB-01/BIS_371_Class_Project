CREATE TABLE People_Department
(People_ID INT NOT NULL,
Department_ID INT NOT NULL);

CREATE TABLE Role
(Role_ID INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
Role_Type_Name VARCHAR(30) NOT NULL);

