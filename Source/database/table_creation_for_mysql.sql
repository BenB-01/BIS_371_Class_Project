CREATE TABLE Department
(Department_ID INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
Department_Name VARCHAR(30) NOT NULL);

CREATE TABLE Paper_Person
(Paper_ID INT NOT NULL,
