CREATE TABLE Paper
(Paper_ID INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
Paper_Title VARCHAR(50) NOT NULL,
Paper_Type INT NOT NULL,
Target INT NOT NULL,
Tier INT NULL);

CREATE TABLE Paper_Type
(Paper_Type_ID INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
Paper_Type_Name VARCHAR(20) NOT NULL);

CREATE TABLE People
(People_ID INT NOT NULL AUTO_INCREMENT PRIMARY KEY,
